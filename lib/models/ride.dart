class Ride {
  final String driverName;
  final String from;
  final String to;
  final String date;
  final String time;
  final int price;
  final int seats;

  Ride({
    required this.driverName,
    required this.from,
    required this.to,
    required this.date,
    required this.time,
    required this.price,
    required this.seats,
  });
}
